function copyToClipboard1() {
    // コピー対象をJavaScript上で変数として定義する
    var copyTarget = document.getElementById("1");

    // コピー対象のテキストを選択する
    copyTarget.select();

    // 選択しているテキストをクリップボードにコピーする
    document.execCommand("Copy");

    // コピーをお知らせする
    
    }


